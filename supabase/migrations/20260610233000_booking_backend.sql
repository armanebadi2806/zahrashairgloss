create extension if not exists pgcrypto;

create table if not exists public.services (
  id text primary key,
  name text not null,
  short_name text not null,
  duration_minutes integer not null check (duration_minutes > 0),
  active boolean not null default true
);

create table if not exists public.blocked_periods (
  id bigint generated by default as identity primary key,
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  reason text not null default 'Frei',
  created_at timestamptz not null default now(),
  check (ends_at > starts_at)
);

create table if not exists public.holds (
  id uuid primary key default gen_random_uuid(),
  service_id text not null references public.services(id),
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  expires_at timestamptz not null,
  status text not null default 'active' check (status in ('active','released','converted','expired')),
  created_at timestamptz not null default now()
);

create table if not exists public.bookings (
  id uuid primary key default gen_random_uuid(),
  hold_id uuid unique references public.holds(id),
  service_id text not null references public.services(id),
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  first_name text not null,
  last_name text not null,
  email text not null,
  phone text not null,
  note text,
  status text not null default 'confirmed' check (status in ('confirmed','cancelled')),
  deposit_cents integer not null default 3000,
  payment_status text not null default 'pending' check (payment_status in ('pending','paid','manual')),
  terms_version text not null,
  terms_accepted_at timestamptz not null,
  created_at timestamptz not null default now()
);

create table if not exists public.notifications (
  id uuid primary key default gen_random_uuid(),
  booking_id uuid references public.bookings(id),
  title text not null,
  message text not null,
  read_at timestamptz,
  created_at timestamptz not null default now()
);

create table if not exists public.admin_users (
  user_id uuid primary key references auth.users(id) on delete cascade,
  created_at timestamptz not null default now()
);

create index if not exists holds_time_idx on public.holds(starts_at, ends_at, status, expires_at);
create index if not exists bookings_time_idx on public.bookings(starts_at, ends_at, status);
create index if not exists blocks_time_idx on public.blocked_periods(starts_at, ends_at);

